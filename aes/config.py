from dataclasses import dataclass

# Размер блока AES в байтах.
# Для всех вариантов AES размер блока фиксирован и равен 128 битам, то есть 16 байтам.
BLOCK_SIZE = 16


@dataclass(frozen=True)
class AESParameters:
    """
    Параметры конкретного варианта AES.
    
    В AES блок данных и ключ рассматриваются не только как набор байтов,
    но и как последовательности 32-битных частей по 4 байта.
    Этот способ представления важен для процедуры расширения ключа, так как
    операции RotWord, SubWord, применение Rcon и вычисление новых элементов
    ключевого расписания определены именно для таких частей.

    Поля:
    - nk: количество 32-битных частей в ключе;
    - nr: количество раундов шифрования;
    - nb: количество 32-битных частей в блоке данных.

    Для AES значение nb всегда равно 4,
    так как размер блока фиксирован и составляет 128 бит.
    """
    nk: int
    nr: int
    nb: int = 4


# Параметры стандартных вариантов AES.
# Ключ словаря — длина ключа в битах.
#
# AES-128:
#   nk = 4 части по 32 бита = 128 бит
#   nr = 10 раундов
# AES-192:
#   nk = 6 частей по 32 бита = 192 бита
#   nr = 12 раундов
# AES-256:
#   nk = 8 частей по 32 бита = 256 бит
#   nr = 14 раундов
AES_VARIANTS: dict[int, AESParameters] = {
    128: AESParameters(nk=4, nr=10),
    192: AESParameters(nk=6, nr=12),
    256: AESParameters(nk=8, nr=14),
}